export function parseLevel(def) {
  const rows = def.rows;
  const H = rows.length;
  const W = Math.max(...rows.map(r => r.length));
  const lvl = {
    name: def.name, sub: def.sub, story: def.story,
    width: W, height: H,
    tiles: new Map(),          // "x,y" -> { t, fallen?, open? }
    spawn: { x: 2, y: 2 },
    guards: [], pickups: [], torches: [], boomboxes: [],
    spikes: [], chompers: [], crumbles: [], plates: [], gateTiles: [],
    gates: [], exit: null,
  };
  const key = (x, y) => x + ',' + y;

  for (let r = 0; r < H; r++) {
    const row = rows[r];
    for (let x = 0; x < W; x++) {
      const ch = x < row.length ? row[x] : '.';
      const y = H - 1 - r;                       // y axis points up
      switch (ch) {
        case '#': lvl.tiles.set(key(x, y), { t: '#' }); break;
        case 'f': lvl.tiles.set(key(x, y), { t: 'f', fallen: false }); lvl.crumbles.push({ x, y }); break;
        case 'g': lvl.tiles.set(key(x, y), { t: 'g', open: false }); lvl.gateTiles.push({ x, y }); break;
        case 's': lvl.spikes.push({ x, y, phase: (x * 0.7) % 2.2 }); break;
        case 'c': lvl.chompers.push({ x, y, phase: (x * 0.45) % 1.6 }); break;
        case 'p': lvl.plates.push({ x, y, pressed: false }); break;
        case 'P': lvl.spawn = { x: x + 0.5, y }; break;
        case 'E': lvl.guards.push({ x: x + 0.5, y, tier: 'street' }); break;
        case 'R': lvl.guards.push({ x: x + 0.5, y, tier: 'elite' }); break;
        case 'B': lvl.guards.push({ x: x + 0.5, y, tier: 'boss' }); break;
        case 'w': lvl.pickups.push({ x: x + 0.5, y: y + 0.5, kind: 'sword' }); break;
        case 'T': lvl.pickups.push({ x: x + 0.5, y: y + 0.5, kind: 'tape' }); break;
        case 'h': lvl.pickups.push({ x: x + 0.5, y: y + 0.5, kind: 'soda' }); break;
        case 'H': lvl.pickups.push({ x: x + 0.5, y: y + 0.5, kind: 'bigsoda' }); break;
        case 'X': lvl.exit = { x, y }; break;
        case '!': lvl.torches.push({ x: x + 0.5, y: y + 0.5 }); break;
        case 'b': lvl.boomboxes.push({ x: x + 0.5, y }); break;
      }
    }
  }

  // Group vertically-adjacent gate tiles into single gates.
  const byCol = new Map();
  for (const t of lvl.gateTiles) {
    if (!byCol.has(t.x)) byCol.set(t.x, []);
    byCol.get(t.x).push(t.y);
  }
  for (const [x, ys] of byCol) {
    ys.sort((a, b) => a - b);
    let run = [ys[0]];
    for (let i = 1; i <= ys.length; i++) {
      if (i < ys.length && ys[i] === ys[i - 1] + 1) { run.push(ys[i]); continue; }
      lvl.gates.push({ x, ys: run.slice(), open: false, anim: 0 });
      if (i < ys.length) run = [ys[i]];
    }
  }
  // Link each plate to the nearest gate to its right (classic PoP), else nearest.
  for (const p of lvl.plates) {
    let best = null, bestD = Infinity;
    for (const g of lvl.gates) {
      const d = g.x - p.x;
      const score = d > 0 ? d : 1000 + Math.abs(d);
      if (score < bestD) { bestD = score; best = g; }
    }
    p.gate = best;
  }
  // Count tapes for HUD.
  lvl.totalTapes = lvl.pickups.filter(p => p.kind === 'tape').length;
  return lvl;
}
